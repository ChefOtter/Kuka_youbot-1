function ee_pose = forwardKin(pos)
    l2 = 0.155;
    l3 = 0.135;
    l4 = 0.218;
    lox = 0.033;
    loz = 0.1472;
    lax = 0;

    cos1 = cos(pos(1));
    sin1 = sin(pos(1));
    sin2 = sin(pos(2));
    cos2 = cos(pos(2));
    sin3 = sin(pos(3));
    cos3 = cos(pos(3));
    sin4 = sin(pos(4));
    cos4 = cos(pos(4));
    cos5 = cos(pos(5));
    sin5 = sin(pos(5));

    xi_1(1, 1) = cos1;
    xi_1(1, 2) = -sin1;
    xi_1(1, 3) = 0;
    xi_1(1, 4) = -lax * (cos1 - 1.0);
    xi_1(2, 1) = sin1;
    xi_1(2, 2) = cos1;
    xi_1(2, 3) = 0;
    xi_1(2, 4) = -lax * sin1;
    xi_1(3, 1) = 0;
    xi_1(3, 2) = 0;
    xi_1(3, 3) = 1.0;
    xi_1(3, 4) = 0;
    xi_1(4, 1) = 0;
    xi_1(4, 2) = 0;
    xi_1(4, 3) = 0;
    xi_1(4, 4) = 1.0;

    xi_2(1, 1) = cos2;
    xi_2(1, 2) = 0;
    xi_2(1, 3) = sin2;
    xi_2(1, 4) = -loz * sin2 - (cos2 - 1.0) * (lax + lox);
    xi_2(2, 1) = 0;
    xi_2(2, 2) = 1.0;
    xi_2(2, 3) = 0;
    xi_2(2, 4) = 0;
    xi_2(3, 1) = -sin2;
    xi_2(3, 2) = 0;
    xi_2(3, 3) = cos2;
    xi_2(3, 4) = sin2 * (lax + lox) - loz * (cos2 - 1.0);
    xi_2(4, 1) = 0;
    xi_2(4, 2) = 0;
    xi_2(4, 3) = 0;
    xi_2(4, 4) = 1.0;

    xi_3(1, 1) = cos3;
    xi_3(1, 2) = 0;
    xi_3(1, 3) = sin3;
    xi_3(1, 4) = -sin3 * (l2 + loz) - (cos3 - 1.0) * (lax + lox);
    xi_3(2, 1) = 0;
    xi_3(2, 2) = 1.0;
    xi_3(2, 3) = 0;
    xi_3(2, 4) = 0;
    xi_3(3, 1) = -sin3;
    xi_3(3, 2) = 0;
    xi_3(3, 3) = cos3;
    xi_3(3, 4) = sin3 * (lax + lox) - (cos3 - 1.0) * (l2 + loz);
    xi_3(4, 1) = 0;
    xi_3(4, 2) = 0;
    xi_3(4, 3) = 0;
    xi_3(4, 4) = 1.0;

    xi_4(1, 1) = cos4;
    xi_4(1, 2) = 0;
    xi_4(1, 3) = sin4;
    xi_4(1, 4) = -(cos4 - 1.0) * (lax + lox) - sin4 * (l2 + l3 + loz);
    xi_4(2, 1) = 0;
    xi_4(2, 2) = 1.0;
    xi_4(2, 3) = 0;
    xi_4(2, 4) = 0;
    xi_4(3, 1) = -sin4;
    xi_4(3, 2) = 0;
    xi_4(3, 3) = cos4;
    xi_4(3, 4) = sin4 * (lax + lox) - (cos4 - 1.0) * (l2 + l3 + loz);
    xi_4(4, 1) = 0;
    xi_4(4, 2) = 0;
    xi_4(4, 3) = 0;
    xi_4(4, 4) = 1.0;

    xi_5(1, 1) = cos5;
    xi_5(1, 2) = -sin5;
    xi_5(1, 3) = 0;
    xi_5(1, 4) = -(cos5 - 1.0) * (lax + lox);
    xi_5(2, 1) = sin5;
    xi_5(2, 2) = cos5;
    xi_5(2, 3) = 0;
    xi_5(2, 4) = -sin5 * (lax + lox);
    xi_5(3, 1) = 0;
    xi_5(3, 2) = 0;
    xi_5(3, 3) = 1.0;
    xi_5(3, 4) = 0;
    xi_5(4, 1) = 0;
    xi_5(4, 2) = 0;
    xi_5(4, 3) = 0;
    xi_5(4, 4) = 1.0;

    tf(1, 1) = 0;
    tf(1, 2) = 0;
    tf(1, 3) = -1.0;
    tf(1, 4) = lax + lox;
    tf(2, 1) = 0;
    tf(2, 2) = 1.0;
    tf(2, 3) = 0;
    tf(2, 4) = 0;
    tf(3, 1) = 1.0;
    tf(3, 2) = 0;
    tf(3, 3) = 0;
    tf(3, 4) = l2 + l3 + l4 + loz;
    tf(4, 1) = 0;
    tf(4, 2) = 0;
    tf(4, 3) = 0;
    tf(4, 4) = 1.0;

    ee_pose = xi_1 * xi_2 * xi_3 * xi_4 * xi_5 * tf;
%     for i = 1:5
%         for j = 1:5
%           ee_pose(i, j) = temp(i, j);
%         end
% 	end
end